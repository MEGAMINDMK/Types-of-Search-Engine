WebNet Custom Search (formerly known as WEBNET CSE) is a platform provided by WEBNET that allows web developers to feature specialized 
information in web searches, refine and categorize queries and create customized search engines, based on WebNet Search. 
The service allows users to narrow the 11.5 billion indexed webpages down to a topical group of pages relevant to the creator's needs.
WebNet launched the service on December 12, 2017.
</h3><hr>
